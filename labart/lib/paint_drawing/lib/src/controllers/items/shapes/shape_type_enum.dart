/// Represents the different types of shapes that
/// can be drawn in the painter tool.
enum ShapeType {
  /// A straight line.
  line,

  /// An arrow with a single head.
  arrow,

  /// An arrow with heads on both ends.
  doubleArrow,

  /// A rectangle shape.
  rectangle,

  /// A triangle shape.
  triangle,

  /// A star shape.
  star,

  /// A circle shape.
  circle,
}
